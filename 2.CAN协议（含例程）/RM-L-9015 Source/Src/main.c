/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
    
    uint32_t pre_tick = 0;

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_CAN_Init();
  MX_TIM17_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  
  Button_Init();
  CanComm_Init();
  HAL_TIM_Base_Start_IT(&htim17);
  
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
      
        BNT1_state = get_bnt1_state();    /**!< 校准按钮 */
        BNT2_state = get_bnt2_state();    /**!< 控制按钮 */
      
        if(BNT2_state == SINGLE_CLICK)
        {
            /* 电机在位置模式下，旋转一圈 */
            CANComm_SetMode(MOTOR1_NODEID, POSITION_MODE);
            HAL_Delay(1);
            CANComm_SetPostion(MOTOR1_NODEID, CPR/2);
        }
        else if(BNT1_state == SINGLE_CLICK)
        {
            /* 电机速度为0 */
            CANComm_SetMode(MOTOR1_NODEID, VELOCITY_Mode);
            HAL_Delay(1);
            CANComm_SetVelocity(MOTOR1_NODEID, 0);
        }
        else if(BNT1_state == DOUBLE_CLICK)
        {
            /* 电机按照指定的速度旋转 */
            CANComm_SetMode(MOTOR1_NODEID, VELOCITY_Mode);
            HAL_Delay(1);
            CANComm_SetVelocity(MOTOR1_NODEID, 3000);
        }
        
        /* 读取当前的位置、速度、电流 */
        if((HAL_GetTick() - pre_tick) >= 100)
        {
            pre_tick = HAL_GetTick();
            HAL_GPIO_TogglePin(LED_RUN_GPIO_Port, LED_RUN_Pin);
            HAL_Delay(1);
            CANComm_GetPosition(MOTOR1_NODEID);
            HAL_Delay(1);
            CANComm_GetVelocity(MOTOR1_NODEID);
            HAL_Delay(1);
            CANComm_GetCurrent(MOTOR1_NODEID);
            HAL_Delay(1);
        }
  }
  /* USER CODE END 3 */
}

